package chap06.book.example.s060804;

public class Calculator {

	// 정사각형의 넓이
	double areaRectangle(double width) {
		return width * width;
	}

	// 직사각형의 넓이
	double areaRectangle(double width, double height) {
		return width * height;
	}
	
	// 매개변수의 타입 개수 순서가 다르면, 같은 이름을 사용가능.(실질적으로는 별개의 메소드)
	
	
}
